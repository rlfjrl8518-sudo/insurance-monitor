"""수집한 광고 데이터를 CSV로 저장/병합하고 상태(신규/운영중/종료)를 관리하는 모듈."""

import csv
import hashlib
import os
from datetime import datetime, timedelta, timezone
from urllib.parse import urlparse

# 한국 표준시 (UTC+9). GitHub Actions 등 UTC 환경에서도 한국 날짜 기준으로 집계하기 위해 사용.
KST = timezone(timedelta(hours=9))


def 오늘_KST():
    """한국 시간(KST) 기준 오늘 날짜를 반환한다."""
    return datetime.now(KST).date()

# CSV 파일의 컬럼 순서
CSV_컬럼 = [
    "ad_id",
    "광고주",
    "구분",
    "이미지파일명",
    "이미지URL",
    "소재유형",
    "보종",
    "후킹",
    "요약",
    "광고텍스트",
    "광고시작일",
    "광고종료일",
    "광고상세URL",
    "수집일",
    "최초발견일",
    "상태",
    "운영일수",
]

상태_신규 = "신규"
상태_운영중 = "운영중"
상태_종료 = "종료"

# 운영일수가 이 일수 미만인 광고는 '신규'로 표시한다.
신규_유지_일수 = 3


def 광고ID_생성(이미지_url):
    """이미지 URL을 기반으로 광고 고유 ID(MD5 해시)를 생성한다.

    Meta CDN 이미지 URL은 매 요청마다 쿼리스트링(서명/만료 토큰)뿐 아니라
    호스트명(scontent-XXX-N.xx.fbcdn.net)도 함께 바뀔 수 있다. 호스트명까지 해시에
    포함하면 같은 광고가 스크랩마다 다른 ad_id를 갖게 되므로, URL의 경로(path)만으로
    해시를 계산해야 같은 광고가 항상 같은 ad_id를 갖는다.
    """
    경로 = urlparse(이미지_url).path
    return hashlib.md5(경로.encode("utf-8")).hexdigest()


def CSV_읽기(csv_경로):
    """CSV 파일을 읽어 ad_id를 키로 하는 딕셔너리로 반환한다. 파일이 없으면 빈 딕셔너리 반환."""
    if not os.path.exists(csv_경로):
        return {}

    결과 = {}
    with open(csv_경로, "r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            결과[row["ad_id"]] = row
    return 결과


def CSV_쓰기(csv_경로, 전체_데이터):
    """ad_id를 키로 하는 딕셔너리를 CSV 파일로 저장한다."""
    os.makedirs(os.path.dirname(csv_경로), exist_ok=True)

    with open(csv_경로, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_컬럼, restval="")
        writer.writeheader()
        for ad_id in sorted(전체_데이터.keys()):
            writer.writerow(전체_데이터[ad_id])


def 운영일수_계산(광고시작일_문자열, 최초발견일_문자열, 오늘=None):
    """광고시작일(라이브 시작일)부터 오늘까지의 일수를 계산한다. (운영일수 = 오늘 - 광고시작일)

    광고시작일 정보가 없는 경우 최초발견일을 대신 사용한다.
    """
    if 오늘 is None:
        오늘 = 오늘_KST()
    기준일_문자열 = 광고시작일_문자열 or 최초발견일_문자열
    기준일 = datetime.strptime(기준일_문자열, "%Y-%m-%d").date()
    return max((오늘 - 기준일).days, 0)


def 상태_계산(운영일수, 진행중_상태):
    """운영일수가 신규_유지_일수 미만이면 '신규', 아니면 진행중_상태(운영중/종료)를 반환한다."""
    if 운영일수 < 신규_유지_일수:
        return 상태_신규
    return 진행중_상태


def 데이터_병합(전체_데이터, 광고주, 구분, 수집된_광고_목록, 오늘=None):
    """특정 광고주에 대해 새로 수집한 광고 목록을 기존 데이터와 병합하고 상태를 갱신한다.

    - 새로 발견된 광고: 최초발견일=오늘, 운영일수가 신규_유지_일수 미만이면 신규, 아니면 운영중
    - 기존에 있고 이번에도 발견된 광고: 운영일수가 신규_유지_일수 미만이면 신규, 아니면 운영중,
      수집일 갱신 (종료됐던 광고가 재등장하면 종료일 제거)
    - 기존에 있었지만 이번에 발견되지 않은 광고: 상태=종료, 종료일=오늘 (이미 종료된 광고는 그대로 유지)

    구분(자사/경쟁사)과 광고주명은 매 수집마다 "설정" 시트 기준으로 결정되므로,
    이번에 다시 만난 기존 광고에도 현재 광고주명/구분을 덮어써서 갱신한다.
    (설정 시트의 광고주 표기가 바뀌어도 다음 수집에서 자동으로 따라가도록 함)

    반환값: (신규_개수, 운영중_개수, 신규종료_개수)
    """
    if 오늘 is None:
        오늘 = 오늘_KST()
    오늘_문자열 = 오늘.strftime("%Y-%m-%d")

    수집된_id_목록 = set()
    신규_개수 = 0
    운영중_개수 = 0
    신규종료_개수 = 0

    for 광고 in 수집된_광고_목록:
        ad_id = 광고["ad_id"]
        수집된_id_목록.add(ad_id)

        if ad_id in 전체_데이터:
            # 기존 광고 -> 운영일수가 신규_유지_일수 미만이면 신규, 아니면 운영중
            기존_행 = 전체_데이터[ad_id]
            기존_행["수집일"] = 오늘_문자열
            기존_행["광고종료일"] = ""
            기존_행["광고주"] = 광고주
            기존_행["구분"] = 구분
            # 광고 텍스트/이미지 등 최신 정보로 갱신
            기존_행["이미지URL"] = 광고["이미지URL"]
            기존_행["이미지파일명"] = 광고["이미지파일명"]
            기존_행["광고텍스트"] = 광고["광고텍스트"]
            기존_행["광고시작일"] = 광고["광고시작일"]
            기존_행["광고상세URL"] = 광고["광고상세URL"]
            기존_행["운영일수"] = str(운영일수_계산(기존_행["광고시작일"], 기존_행["최초발견일"], 오늘))
            기존_행["상태"] = 상태_계산(int(기존_행["운영일수"]), 상태_운영중)
            운영중_개수 += 1
        else:
            # 새로 발견된 광고 -> 운영일수가 신규_유지_일수 미만이면 신규, 아니면 운영중
            운영일수 = 운영일수_계산(광고["광고시작일"], 오늘_문자열, 오늘)
            전체_데이터[ad_id] = {
                "ad_id": ad_id,
                "광고주": 광고주,
                "구분": 구분,
                "이미지파일명": 광고["이미지파일명"],
                "이미지URL": 광고["이미지URL"],
                "소재유형": "",
                "보종": "",
                "후킹": "",
                "요약": "",
                "광고텍스트": 광고["광고텍스트"],
                "광고시작일": 광고["광고시작일"],
                "광고종료일": "",
                "광고상세URL": 광고["광고상세URL"],
                "수집일": 오늘_문자열,
                "최초발견일": 오늘_문자열,
                "상태": 상태_계산(운영일수, 상태_운영중),
                "운영일수": str(운영일수),
            }
            신규_개수 += 1

    # 이번 수집에서 발견되지 않은 기존 광고 -> 종료 처리
    for ad_id, 행 in 전체_데이터.items():
        if 행["광고주"] != 광고주:
            continue
        행["구분"] = 구분
        if ad_id in 수집된_id_목록:
            continue
        if 행["상태"] != 상태_종료:
            행["상태"] = 상태_종료
            행["광고종료일"] = 오늘_문자열
            신규종료_개수 += 1
        # 종료된 광고도 운영일수는 계속 갱신 (오늘 - 광고시작일)
        행["운영일수"] = str(운영일수_계산(행["광고시작일"], 행["최초발견일"], 오늘))

    return 신규_개수, 운영중_개수, 신규종료_개수


