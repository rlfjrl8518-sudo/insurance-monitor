"""광고 텍스트의 언어를 판별하는 모듈."""

import re

# 한글 음절(가-힣) 포함 여부를 검사하는 정규식
한글_정규식 = re.compile(r"[가-힣]")


def 한국어_포함(텍스트):
    """텍스트에 한글 음절이 하나 이상 포함되어 있으면 True를 반환한다."""
    return bool(한글_정규식.search(텍스트 or ""))


def 외국어_소재인가(광고텍스트):
    """광고 텍스트가 비어있지 않은데 한글이 전혀 없으면(영어/중국어 등 외국어 소재) True를 반환한다."""
    광고텍스트 = (광고텍스트 or "").strip()
    if not 광고텍스트:
        return False
    return not 한국어_포함(광고텍스트)
