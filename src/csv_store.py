"""수집한 광고 데이터를 CSV로 저장/병합하고 상태(신규/운영중/종료)를 관리하는 모듈."""

import csv
import hashlib
import os
from datetime import date, datetime

# CSV 파일의 컬럼 순서
CSV_컬럼 = [
    "ad_id",
    "광고주",
    "이미지파일명",
    "이미지URL",
    "소재유형",
    "보종",
    "후킹",
    "요약",
    "광고텍스트",
    "광고시작일",
    "광고종료일",
    "광고상세URL",
    "수집일",
    "최초발견일",
    "상태",
    "운영일수",
]

상태_신규 = "신규"
상태_운영중 = "운영중"
상태_종료 = "종료"


def 광고ID_생성(이미지_url):
    """이미지 URL을 기반으로 광고 고유 ID(MD5 해시)를 생성한다.

    Meta CDN 이미지 URL에는 매 요청마다 바뀌는 서명/만료 토큰이 쿼리스트링으로 붙기 때문에,
    쿼리스트링을 제외한 기본 경로만으로 해시를 계산해야 같은 광고가 항상 같은 ad_id를 갖는다.
    """
    기본_url = 이미지_url.split("?")[0]
    return hashlib.md5(기본_url.encode("utf-8")).hexdigest()


def CSV_읽기(csv_경로):
    """CSV 파일을 읽어 ad_id를 키로 하는 딕셔너리로 반환한다. 파일이 없으면 빈 딕셔너리 반환."""
    if not os.path.exists(csv_경로):
        return {}

    결과 = {}
    with open(csv_경로, "r", encoding="utf-8-sig", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            결과[row["ad_id"]] = row
    return 결과


def CSV_쓰기(csv_경로, 전체_데이터):
    """ad_id를 키로 하는 딕셔너리를 CSV 파일로 저장한다."""
    os.makedirs(os.path.dirname(csv_경로), exist_ok=True)

    with open(csv_경로, "w", encoding="utf-8-sig", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=CSV_컬럼, restval="")
        writer.writeheader()
        for ad_id in sorted(전체_데이터.keys()):
            writer.writerow(전체_데이터[ad_id])


def 운영일수_계산(최초발견일_문자열, 오늘=None):
    """최초발견일부터 오늘까지의 일수를 계산한다. (운영일수 = 오늘 - 최초발견일)"""
    if 오늘 is None:
        오늘 = date.today()
    최초발견일 = datetime.strptime(최초발견일_문자열, "%Y-%m-%d").date()
    return (오늘 - 최초발견일).days


def 데이터_병합(전체_데이터, 광고주, 수집된_광고_목록, 오늘=None):
    """특정 광고주에 대해 새로 수집한 광고 목록을 기존 데이터와 병합하고 상태를 갱신한다.

    - 새로 발견된 광고: 상태=신규, 최초발견일=오늘
    - 기존에 있고 이번에도 발견된 광고: 상태=운영중, 수집일 갱신 (종료됐던 광고가 재등장하면 종료일 제거)
    - 기존에 있었지만 이번에 발견되지 않은 광고: 상태=종료, 종료일=오늘 (이미 종료된 광고는 그대로 유지)

    반환값: (신규_개수, 운영중_개수, 신규종료_개수)
    """
    if 오늘 is None:
        오늘 = date.today()
    오늘_문자열 = 오늘.strftime("%Y-%m-%d")

    수집된_id_목록 = set()
    신규_개수 = 0
    운영중_개수 = 0
    신규종료_개수 = 0

    for 광고 in 수집된_광고_목록:
        ad_id = 광고["ad_id"]
        수집된_id_목록.add(ad_id)

        if ad_id in 전체_데이터:
            # 기존 광고 -> 운영중으로 갱신
            기존_행 = 전체_데이터[ad_id]
            기존_행["수집일"] = 오늘_문자열
            기존_행["상태"] = 상태_운영중
            기존_행["광고종료일"] = ""
            기존_행["운영일수"] = str(운영일수_계산(기존_행["최초발견일"], 오늘))
            # 광고 텍스트/이미지 등 최신 정보로 갱신
            기존_행["이미지URL"] = 광고["이미지URL"]
            기존_행["이미지파일명"] = 광고["이미지파일명"]
            기존_행["광고텍스트"] = 광고["광고텍스트"]
            기존_행["광고시작일"] = 광고["광고시작일"]
            기존_행["광고상세URL"] = 광고["광고상세URL"]
            운영중_개수 += 1
        else:
            # 새로 발견된 광고 -> 신규
            전체_데이터[ad_id] = {
                "ad_id": ad_id,
                "광고주": 광고주,
                "이미지파일명": 광고["이미지파일명"],
                "이미지URL": 광고["이미지URL"],
                "소재유형": "",
                "보종": "",
                "후킹": "",
                "요약": "",
                "광고텍스트": 광고["광고텍스트"],
                "광고시작일": 광고["광고시작일"],
                "광고종료일": "",
                "광고상세URL": 광고["광고상세URL"],
                "수집일": 오늘_문자열,
                "최초발견일": 오늘_문자열,
                "상태": 상태_신규,
                "운영일수": "0",
            }
            신규_개수 += 1

    # 이번 수집에서 발견되지 않은 기존 광고 -> 종료 처리
    for ad_id, 행 in 전체_데이터.items():
        if 행["광고주"] != 광고주:
            continue
        if ad_id in 수집된_id_목록:
            continue
        if 행["상태"] != 상태_종료:
            행["상태"] = 상태_종료
            행["광고종료일"] = 오늘_문자열
            신규종료_개수 += 1
        # 종료된 광고도 운영일수는 계속 갱신 (오늘 - 최초발견일)
        행["운영일수"] = str(운영일수_계산(행["최초발견일"], 오늘))

    return 신규_개수, 운영중_개수, 신규종료_개수
